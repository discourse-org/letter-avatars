    when search_char(NOTO_DISPLAY_RANGES, char_code)
        'display'
    when search_char(NOTO_MONO_RANGES, char_code)
        'mono'
    when search_char(NOTO_CJK_RANGES, char_code)
        'cjk'
    when search_char(NOTO_ARABIC_RANGES, char_code)
        'arabic'
    when search_char(NOTO_DEVAGANARI_RANGES, char_code)
        'devaganari'
    when search_char(NOTO_BENGALI_RANGES, char_code)
        'bengali'
    when search_char(NOTO_JAVANESE_RANGES, char_code)
        'javanese'
    when search_char(NOTO_TELUGU_RANGES, char_code)
        'telugu'
    when search_char(NOTO_THAI_RANGES, char_code)
        'thai'
    when search_char(NOTO_HEBREW_RANGES, char_code)
        'hebrew'
    when search_char(NOTO_ARMENIAN_RANGES, char_code)
        'armenian'
    when search_char(NOTO_EMOJI_RANGES, char_code)
        'emoji'
    else
        'none'
    end
  end

  def self.search_char(range, char_code)
    !!range.reverse.bsearch{|x| x <=> char_code }
  end
